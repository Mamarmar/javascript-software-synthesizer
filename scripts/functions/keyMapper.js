export default function keyMapper(key, base) {
  switch (key) {
    case "a":
      return base;
      break;
    case "w":
      return base + 1;
      break;
    case "s":
      return base + 2;
      break;
    case "e":
      return base + 3;
      break;
    case "d":
      return base + 4;
      break;
    case "f":
      return base + 5;
      break;
    case "t":
      return base + 6;
      break;
    case "g":
      return base + 7;
      break;
    case "y":
      return base + 8;
      break;
    case "h":
      return base + 9;
      break;
    case "u":
      return base + 10;
      break;
    case "j":
      return base + 11;
      break;
    case "k":
      return base + 12;
      break;
    case "o":
      return base + 13;
      break;
    case "l":
      return base + 14;
      break;
    case "p":
      return base + 15;
      break;
  }
}